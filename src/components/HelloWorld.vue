<template>
  <div class="container-fluid">
    <h1>{{ titulo }}</h1>

    <div
      class="alert alert-warning"
      role="alert"
      v-show="usuarios.length === 0"
    >Não existem registros de usuários !</div>

    <table class="table table-striped" v-show="usuarios.length > 0">
      <thead>
        <tr>
          <th>#</th>
          <th>Nome</th>
          <th></th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="(usuario, index) in usuarios" v-bind:key="usuario.id">
          <td>{{ usuario.id }}</td>
          <td>{{ usuario.nome }}</td>
          <td>
            <button class="btn btn-danger" v-on:click="deletar(index)">Remover</button>
          </td>
        </tr>
      </tbody>
    </table>

    <div class="form-group">
      <p>
        <input
          placeholder="Digite um codigo"
          type="text"
          v-model="id"
          name="idUsuario"
          class="form-control"
        />
      </p>
      <p>
        <input
          placeholder="Digite um nome"
          type="text"
          v-model="nome"
          name="nomeUsuario"
          class="form-control"
        />
      </p>
      <button class="btn btn-primary" v-on:click="adicionar()">Adicionar</button>
    </div>
  </div>
</template>

<script>
export default {
  name: "HelloWorld",
  props: {
    msg: String
  },
  data() {
    return {
      titulo: "Teste VueJS Jonas",
      usuarios: [
        { id: 1, nome: "Joao" },
        { id: 2, nome: "Luiz" },
        { id: 3, nome: "Pedro" },
        { id: 4, nome: "Maria" }
      ],
      id: "",
      nome: ""
    };
  },
  methods: {
    deletar(parametro) {
      this.usuarios.splice(parametro, 1);
    },
    adicionar() {
      this.usuarios.push({
        id: this.id,
        nome: this.nome
      });

      this.id = "";
      this.nome = "";
    }
  }
};
</script>

